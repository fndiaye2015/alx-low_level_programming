#include<stdio.h>
/**
 * main - Entry point
 * Description - This program prints the alphabet in lower case and uppercase
 * Return: 0
 */
int main(void)
{
	char c = 'a';
	char C = 'A';

	while (c <= 'z')
	{
		putchar(c);
		c++;
	}
	while (C <= 'Z')
	{
		putchar(C);
		C++;
	}
	putchar('\n');
	return (0);
}
